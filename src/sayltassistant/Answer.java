package sayltassistant;

public class Answer {
	
	private String answerString;

	public Answer() {
		answerString = "";
	}

	public Answer(String answerString) {
		super();
		this.answerString = answerString;
	}

	public String getAnswerString() {
		return answerString;
	}

	public void setAnswerString(String answerString) {
		this.answerString = answerString;
	}
	
}
